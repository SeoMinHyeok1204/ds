class Calc:
    def sum(self, a, b):
        result = a+b
        print(str(a)+" + "+str(b)+" = "+str(result)+"입니다.")
    def sub(self, a, b):
        result = a-b
        print(str(a)+" - "+str(b)+" = "+str(result)+"입니다.")
    def multi(self, a, b):
        result = a*b
        print(str(a)+" * "+str(b)+" = "+str(result)+"입니다.")
    def div(self, a, b):
        result = a/b
        print(str(a)+" / "+str(b)+" = "+str(result)+"입니다.")
